enum state_t
{
    IDLE = 0,
    WPS_START = 1,
    WPS_WAIT = 2,
};

static state_t currentState = IDLE;
static time_t wpsStart;
static uint16_t lastColor = 0;

static esp_wps_config_t wps_config;

void WifiButton::redraw()
{
    if (!lastColor)
        return;

    s_display.drawRGBBitmap(m_x, m_y, wifi, 40, 40);
}

void WifiButton::touch(const Point &pt)
{
    currentState = WPS_START;
    m_mustDraw = true;
}

void endWps()
{
    esp_wifi_wps_disable();

    WiFi.begin();

    currentState = IDLE;
}

bool WifiButton::isConnected()
{
    return currentState == IDLE && WiFi.status() == WL_CONNECTED;
}

void WifiButton::loop()
{
    ScreenArea::loop();

    auto color =
        currentState == WPS_START || currentState == WPS_WAIT
            ? ILI9341_YELLOW
        : WiFi.status() == WL_CONNECTED
            ? ILI9341_GREEN
            : ILI9341_RED;

    if (color != lastColor)
    {
        lastColor = color;

        for (auto row = 0; row < 40; row++)
            for (auto col = 0; col < 40; col++)
            {
                auto index = 40 * row + col;

                if (wifi[index])
                    wifi[index] = color;
            }

        m_mustDraw = true;
    }

    switch (currentState)
    {
    case WPS_START:
        esp_wifi_wps_enable(&wps_config);
        esp_wifi_wps_start(0);

        wpsStart = time(nullptr);

        currentState = WPS_WAIT;
        m_mustDraw = true;

        break;
    case WPS_WAIT:
    {
        if (difftime(time(nullptr), wpsStart) >= 30)
            endWps();

        break;
    }
    }
}

void WifiButton::setup()
{
    ScreenArea::setup();

    memset(&wps_config, 0, sizeof(wps_config));

    wps_config.wps_type = WPS_TYPE_PBC;

    WiFi.onEvent(
        [](WiFiEvent_t event, arduino_event_info_t info)
        {
            switch (event)
            {
            case ARDUINO_EVENT_WIFI_STA_DISCONNECTED:
                WiFi.reconnect();
                break;
            case ARDUINO_EVENT_WPS_ER_SUCCESS:
                endWps();
                break;
            }
        });

    WiFi.mode(WIFI_MODE_STA);

    WiFi.begin();
}